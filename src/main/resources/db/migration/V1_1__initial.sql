CREATE SEQUENCE VEDTAK_UTKAST_SEQ;

CREATE TABLE VEDTAK_UTKAST (
  VEDTAK_UTKAST_ID        NUMBER,
  AKTOR_ID                VARCHAR(20) NOT NULL,
  VEKTLAGT_OPPLYSNINGER   CLOB,
  HOVEDMAL                ENUM('SKAFFE_ARBEID', 'BEHOLDE_ARBEID'),
